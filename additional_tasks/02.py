number_1 = int(input("Введіть перше число: "))
number_2 = int(input("Введіть друге число: "))

print("Сума:", number_1 + number_2)
print("Різниця:", number_1 - number_2)
print("Добуток:", number_1 * number_2)
print("Ділення:", number_1 / number_2)
print("Залишок від ділення:", number_1 % number_2)
print("Перше число у ступені другого числа:", number_1 ** number_2)